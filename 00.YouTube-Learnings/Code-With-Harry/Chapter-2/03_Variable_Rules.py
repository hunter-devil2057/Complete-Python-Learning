# Rules for Writing/Defining Variable Name
    # - A variable name can contain alphabets, digits and underscores. 
    # - A variable name can only start with an alphabet and underscores
    # - A variable name can't start with a digit
    # - No while space is allowed to be used inside a variable name. 

a=23        #valid

aaa=435     #valid 

manish=23   #valid

harish=45   #valid

_manish=34  #valid

# @sameer=467       #Not valid
# No special characters except (underscore(_)) are used to define variable name

# 1manish="Teacher"       #Not Valid
# No numeric digit can be used to start variable name

# animal name="Penguin"         #Not Valid
# No White Space is not allowed
