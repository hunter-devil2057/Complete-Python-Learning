# Type() Function
# type() function: is used to find the data type of a given variable in python.. 

# Integer
a=31
t=type(a)     #class<int>
print(t)

# Floating Numbers
b=31.45
u=type(b)
print(u)

# String
c="Manish"
c1="21.4"
c2="21"
v=type(c)
v=type(c1)
v=type(c2)
print(v)

# Boolean
d=True
w=type(d)
print(w)

# None
e=None
x=type(e)
print(x)